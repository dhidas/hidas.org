<p>
  My area of research is in experimental high energy particle physics.  I have worked in Higgs physics, electroweak physics, and searches for exotic signatures.  My current focus is on building a diamond-based pixel tracking detector which will be used for an online high percision luminosity measurement in CMS at the LHC.  Along with this I study the properties of diamond and silicon as a sensor materials and radiation effects on sensor performance.
</p>

<p>
  I am interested in designing and building new detectors for new experiments which probe our fundamental understanding of particle interactions in nature.  What is the newly discovered boson at the LHC (see a <a href="http://prl.aps.org/abstract/PRL/v110/i8/e081803">recent CMS publication</a>)?  Are dark photons a viable and observable dark matter candidate?  Are neutrinos in fact majorana fermions?  If lepton flavor violation occurs in the neutrino sector could it also be that it occurs at some very small level in the charged lepton sector?
</p>

<p>
  In general I work on a variety of things including physics analysis software, statistics packages for analysis, diamond and silicon sensors, interface and controls software, detector calibration software, data acquisition, simulation, and event reconstruction and analysis frameworks.
</p>

<p>
  Related things which I am interested in include: Probability and statistics, modeling of large-scale complex systems, programming, programming languages, cosmology, dark matter, and anything related to why the scale of the large has not met that of the small.  Global warming and earth science in general.  Also, space exploration.
</p>

