<h3>Research</h3>

<p>
  My current research focuses on insertion devices for applications at sycnrotron facilities.  Our group is responsible for the insertion devices for the National Synchrotron Light Source II at Brookhaven National Laboratory.  I am interested in the simulation of magnetic devices installed in modern light sources, advanced computing techniques (cluster, cloud, open science grid, multi-threaded, GPU), algorithm development, and open source scientific advancement.  I am the author of <a href="http://oscars.bnl.gov">OSCARS</a>.
</p>

<p>
  In general my research in the past has focused on searches for the higgs boson, measurements of electroweak production, searches for exotic particles, and building detectors for particle physics applications.  I am also interested in a wide variety of things ranging from cosmology to earth science to space exploration.  My current work focuses on Insertion Devices for the NSLS II 3 GeV electron synchrotron.
</p>



<h4>NSLSII</h4>

<p>
  I am a member of Insertion Device group in the Accelerator Division of NSLSII.  We are responsible for the procurement, assembly, magnetic measurement, testing, installation, and maintenance of all insertion devices in the NSLSII storage ring.  These include wigglers, undulators, elliptically polarizing undulators (EPUs), three pole wigglers, in-vacuum undulators (IVUs), and in the future possibly superconducting undulators (SCU) and cryogenic permanent magnet undulators (CPMU).  We use extensively EPICS, Delta Tau pmac motion controllers, and write hardware specific drivers for various laboratory equipment.  I am interested in the theory and phenomenology of synchrotron radiation and have written from first principles the radiation simulation software called <a href="http://oscars.bnl.gov">OSCARS</a>.
</p>


<h4>CMS</h4>

<p>
  I was a member of CMS from 2009 to 2015.  I was involved in several searches for new physics looking for resonances in multi-jet final states.  I wrote statistical analysis packages to interpret and quantify what we see in the data.  A majority of my time was spent on the development, construction, and installation of a new detector for CMS, the Pixel Luminosity Telescope (PLT).  I have also worked as an expert on the CMS Pixel detector online and offline data quality monitoring.
</p>

<p>
  I was Technical Coordinator for the CMS Pixel Luminosity Telescope.  This is a pixelated silicon tracking detector designed to measure the bunch-to-bunch luminosity in the LHC with high precision and speed and was fully installed in CMS in early 2015.  We had a pilot run phase (from January 2012 to February 2013) in the LHC with one quarter of the full detector with single-crystal diamond sensors.  This pilot detector was the first of its kind built and the first such device to run in conditions like the LHC.  I am the original author of the <a href="https://github.com/cmsplt">cmsplt</a> and <a href="https://github.com/cmsplt/PLTOffline">PLTOffline</a> codes.
</p>

<p>
  On the PLT I worked on many different things including detector construction, sensor testing, tracking and reconstruction software, designing analysis frameworks, monitoring and measuring detector properties and performance in various conditions, and was responsible for daily operations.  I have been involved in many testbeams to test detector performance, DAQ, rate effects, etc.
</p>



<h4>CDF</h4>

<p>
  On CDF my research has mainly focused on Higgs and electoweak physics.  My <a href="http://lss.fnal.gov/archive/thesis/2000/fermilab-thesis-2008-83.pdf">thesis</a> was a search for a high-mass Higgs boson decaying to W-boson pairs, the final states consisting of 2 leptons and missing energy from the escaping neutrinos.  This search made use of matrix element calculations as well as neural networks.
</p>

<p>
  As an aside to the Higgs search I have been involved in measuring the production rate of W-boson pairs and search for anomalous couplings.  In this study a matrix element based likelihood ratio is used to differentiate the WW signal of interest from backgrounds.  The cross section is measured from a fit to the likelihood ratio while the leading lepton transverse momentum distribution is used to set limits on anomalous couplings.
</p>

<p>
  I have also been involved in an analysis which measured the WW, top-pair, and Z->tautau, cross sections simultaneously in the dilepton sample.  This is done via a simultaneous fit using a missing transverse energy vs. number of jets phase space.
</p>

<p>
  My service work on CDF involved the silicon tracker.  More specifically I was involved in silicon power supply monitoring and control systems software interfaces.
</p>


